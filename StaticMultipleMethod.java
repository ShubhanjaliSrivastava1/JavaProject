package Java_project;

public class StaticMultipleMethod
{
	static void method1()
	{
		System.out.println("Vaibhav");
	}
	static void method2()
	{
		System.out.println("Shubhanjali");
	}
	static void method3()
	{
		System.out.println("Anjali");
	}
	static void method4()
	{
		System.out.println("Sonu");
	}
	static void method5()
	{
		System.out.println("Raju");
	}

	public static void main(String[] args)
	{
		System.out.println("Swarna");
		method1();
		method2();
		method3();
		method4();
		method5();
		
		
		
	}

}
