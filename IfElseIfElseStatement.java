package Java_project;

public class IfElseIfElseStatement 
{

	public static void main(String[] args) 
	{
		int a=100;
		int b=200;
		int c=250;
		if (a!=b)
		{
			System.out.println("If Block");
		}
		else if (b!=c)
		{
			System.out.println("Else If Block");
		}
		else 
		{
			System.out.println("Else Block");
		}
			

	}

}
